export class Singer {
  public nameSinger: string;
  public birthDay: any;
  public description: string;
  public avatarSinger: string;

  constructor(nameSinger: string, birthDay: any, description: string, avatarSinger: string) {
    this.nameSinger = nameSinger;
    this.birthDay = birthDay;
    this.description = description;
    this.avatarSinger = avatarSinger;
  }
}
